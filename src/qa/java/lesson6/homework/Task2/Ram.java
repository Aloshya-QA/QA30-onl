package qa.java.lesson6.homework.Task2;

public class Ram {
    private int volume;
    private String name;

    public Ram() {
    }

    public Ram(int volume, String name) {
        this.volume = volume;
        this.name = name;
    }

    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
